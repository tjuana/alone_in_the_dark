int	ft_atoi(char *s)
{
	long	r = 0;
	int		sign = 1;
	
	while (*s == 32 ||( *s >= 9 && *s<= 13))
		s++;
	if (*s == '-')
		sign = -1;
	if (*s == '+' || *s == '-')
		s++;
	while (*s && (*s >= '0' && *s <= '9'))
	{
		r = r * 10;
		r += (*s++) - '0';
		if (r > (r * 10))
			return (sign < 0) ? (0) : (-1);
	}
	return (r * sign);
}

void	print_hex(int n)
{
	if (n > 16)
		print_hex(n / 16);
	n = n % 16;
	n += (n > 9) ? 'a' - 10 : '0';
	write(1, &n, 1);
}
int		main(int c, char **v)
{
	if (c == 2)
		print_hex(ft_atoi(v[1]));
	write(1, "\n", 1);
	return (0);
}
