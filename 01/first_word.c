#include <unistd.h>

int	main(int c, char **v)
{
	if (c == 2)
	{
		while (*v[1] && (*v[1] == 32 || *v[1] >= '\t'))
		   v[1]++;
		while (*v[1] != '\0' && (*v[1] != 32 && *v[1] != '\t'))
			write(1, v[1]++, 1);	
	}
	write(1, "\n", 1);
	return (0);
}
