#include <stdlib.h>
#include <unistd.h>

int	main(int c, char **v)
{
	if (v[2][1] != '\0' || v[3][1] != '\0')
	{
		write(1, "\n", 1);
		return (0);	
	}
	if (c == 4)
	{
		while (*v[1])
		{
			if (*v[1] == v[2][0])
				*v[1] = v[3][0];
			write(1, v[1]++, 1);
		}
	}
	write(1, "\n", 1);
	return (0);
}
