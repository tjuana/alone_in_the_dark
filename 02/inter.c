#include <unistd.h>

void	inter(char *s1, char *s2)
{
	int i = 0;
	int ii[256] = {0};

	while (s2[i])
	{
		if (ii[(int)s2[i]] == 0)
			ii[(int)s2[i]] = 1;
		i++;
	}
	i = 0;
	while (s1[i])
	{
		if (ii[(int)s1[i]] == 1)
		{
			ii[(int)s1[i]] = 2;
			write(1, &s1[i], 1);
		}
		i++;
	}
}

int	main(int c, char **v)
{
	if (c == 3)
		inter(v[1], v[2]);
	write(1, "\n", 1);
	return (0);
}
