#include <unistd.h>

void	wd_match(char *s1, char *s2)
{
	int i = 0;
	int j = 0;

	while (s1[j])
		j++;
	while (*s2)
	{
		if (s1[i] == *s2)
			i++;
		s2++;
	}
	if (i == j)
		write(1, s1, j);
}

int	main(int c, char **v)
{
	if (c == 3)
		wd_match(v[1], v[2]);
	write(1, "\n", 1);
	return (0);
}
