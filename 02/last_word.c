#include <unistd.h>

int	space(char c)
{
	return ((c == 32 || (c >= 9 && c <= 13))); 
}

int	main(int c, char **v)
{
	if (c == 2)
	{
		while (*v[1])
			v[1]++;
		v[1]--;
		while (*v[1] && space(*v[1]))
			v[1]--;
		while (*v[1] && !space(*v[1]))
			v[1]--;
		v[1]++;
		while (*v[1] && !space(*v[1]))
			write(1, v[1]++, 1);
	}
	write(1, "\n", 1);
	return (0);
}
