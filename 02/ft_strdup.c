char    *ft_strdup(char *src)
{
	int		len = 0;
	int		i = 0;
	char 	*s1;

	while (src[len] != '\0')
		len++;
	if (!(s1 = (char *)malloc(sizeof(char) * (len + 1))))
		return (NULL);
	while (src[i] != '\0')
	{
		s1[i] = src[i];
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
